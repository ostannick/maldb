Route::get('/queue', function () {

    $jobs = \DB::table('jobs')->get();
    
    foreach($jobs as $j)
    {
      $j->payload = json_decode($j->payload);
    }
    
    $failed_jobs = \DB::table('failed_jobs')->orderBy('failed_at', 'desc')->limit(10)->get();

    return view('queue')->with([
      'jobs' => $jobs,
      'failed_jobs' => $failed_jobs,
    ]);
});

