Route::post('/analysis', function(Request $request) {
  $match = $request->input('protein');

  $peptides = DB::select(DB::raw('select * FROM `ecolik12_2_1` WHERE `parent` = ' . '\'' . $match . '\' AND `missed_cleavages` = 0'));

  return json_encode($peptides);
});

